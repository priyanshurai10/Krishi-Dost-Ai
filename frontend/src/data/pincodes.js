export const findLocationByPincode = (code) => {
  const cleanCode = String(code).trim();
  for (const stateObj of INDIA_STATES_DATA) {
    for (const dist of stateObj.districts) {
      if (dist.pincode === cleanCode) {
        return {
          district: dist.name,
          state: stateObj.state,
          country: "India",
          pincode: dist.pincode
        };
      }
    }
  }
  return null;
};
